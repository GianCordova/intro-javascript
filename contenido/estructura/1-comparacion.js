/**
 * Operadores de Comparacion
 */

//- ==: Igualdad
//- ===: Para comparar igualdad en valor Y en tipo de dato
//- !=: "Es diferente a", true o false
//- !==: Es diferente en valor Y en tipo de dato

//- <: Menor que
//- >:Mayor que
//- <=: Menor e igual que
//- >=: Mayor e igual que

const a = 10;
const b = 20;
const c = "10"

console.log(a == b) //false
console.log(a === b) //fasle
console.log(a == c) //true
console.log(a != c) //false -> Porque si son iguales
console.log(a !== c) //true -> Porque son diferentes en tipo de dato


